export function t(key, params = {}) {
    let text = translations[currentLanguage]?.[key] || translations['zh-CN']?.[key] || key;
    
    // æ›¿æ¢å‚æ•°
    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });
    
    return text;
}

